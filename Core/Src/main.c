/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{

  /* USER CODE BEGIN 1 */
  hid_report_buffer[0] = 0;
  hid_report_buffer[1] = 100;
  hid_report_buffer[2] = 0;
  hid_report_buffer[3] = 0;

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_CRC_Init();
  MX_USART3_UART_Init();
  /* USER CODE BEGIN 2 */
  // start logging to console
  init_dma_logging();

  printf("\033c");
  printf("DUVITECH LLC\r\n");
  printf("USB Composite Demo LLC\r\n");

  printf("Initializing Composite USB Device\r\n");

  /* Initialize the USB Device Library */
  if (USBD_Init(&hUsbDeviceFS, &Class_Desc, 0) != USBD_OK)
  {
    Error_Handler();
  }

  /* Store HID Instance Class ID */
  HID_InstID = hUsbDeviceFS.classId;

  /* Register the HID Class */
  if (USBD_RegisterClassComposite(&hUsbDeviceFS, USBD_HID_CLASS, CLASS_TYPE_HID, &HID_EpAdd_Inst) != USBD_OK)
    Error_Handler();

  /* Store the HID Class */
  CDC_InstID = hUsbDeviceFS.classId;

  /* Register CDC Class First Instance */
  if (USBD_RegisterClassComposite(&hUsbDeviceFS, USBD_CDC_CLASS, CLASS_TYPE_CDC, CDC_EpAdd_Inst) != USBD_OK)
    Error_Handler();

  /* Add CDC Interface Class */
  if (USBD_CMPSIT_SetClassID(&hUsbDeviceFS, CLASS_TYPE_CDC, 0) != 0xFF)
  {
    USBD_CDC_RegisterInterface(&hUsbDeviceFS, &USBD_Interface_fops);
  }

  USBD_Start(&hUsbDeviceFS);
  button_pressed_flag = 0;
  /* USER CODE END 2 */

  /* Initialize leds */
  BSP_LED_Init(LED_GREEN);
  BSP_LED_Init(LED_BLUE);
  BSP_LED_Init(LED_RED);

  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  printf("Composite USB Device Ready!\r\nPress USER Button to send Message on VCP and HID\r\n");
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    if (button_pressed_flag == 1)
    {
      USBD_HID_SendReport(&hUsbDeviceFS, hid_report_buffer, 4, HID_InstID);
      CDC_Transmit(&hUsbDeviceFS, (uint8_t*)"Hello\r\n", 7, CDC_InstID);
      HAL_Delay(100);
      button_pressed_flag = 0;
    }
    else
    {
      BSP_LED_Toggle(LED_BLUE);
      HAL_Delay(250);
    }
  }
  /* USER CODE END 3 */
}

